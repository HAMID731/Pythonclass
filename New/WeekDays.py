day_number = int(input("Input a day number (1-7): "))
if day_number == 1:
        print("MONDAY")
elif day_number == 2:
        print("TUESDAY")
elif day_number == 3:
        print("WEDNESDAY")
elif day_number == 4:
        print("THURSDAY")
elif day_number == 5:
        print("FRIDAY")
elif day_number == 6:
        print("SATURDAY")
elif day_number == 7:
        print("SUNDAY")
else:
        print("Invalid day number. Please enter a number between 1 and 7.")

